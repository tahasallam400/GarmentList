import Foundation
import Localize_Swift
extension String{
    class Localization{
        
        class var NoDataExist:String{
            return "No Data Exist".localized()
        }
        
        class var Alpha:String{
            return "Alpha".localized()
        }
        
        class var CreationTime:String{
            return "Creation Time".localized()
        }
        
        class var List:String{
            return "List".localized()
        }
        
        class var GarmentName:String{
            return "Garment Name".localized()
        }
        
        class var Add:String{
            return "Add".localized()
        }
        
        
    }
}
